-module(tweet_processing).

-export([work_handler/0]).

%%%===================================================================
%%% External functions
%%%===================================================================

work_handler() ->
	fun work/1.

%%%===================================================================
%%% Internal functions
%%%===================================================================

work(Tweet) ->
	#{<<"id">> := ID} = Tweet,

	gen_server:cast(aggregator, {tweet, ID, Tweet}).

